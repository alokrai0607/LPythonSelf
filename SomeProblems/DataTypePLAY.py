## Numeric Types:

# Integer
num_int = 1008
print(num_int)

# Float
num_float = 6.1
print(num_float)

# Complex
num_complex = 1 + 5j
print(num_complex)




## Sequence Types:

# String
my_string = "Hello Sir"
print(my_string)

# List
my_list = [1, 2, 3, 4, 5]
print(my_list)

# Tuple
my_tuple = ('apple', 'banana', 'cherry')
print(my_tuple)


## Mapping Type:

# Dictionary
my_dict = {'name': 'Alok Rai', 'age': 28, 'city': 'Varanasi'}
print(my_dict)


## Set Types:

# Set
my_set = {1, 2, 3, 4, 5, 4 , 3 , 2}
print(my_set)

# Frozenset
my_frozenset = frozenset({10, 20, 30 , 60 , 80})
print(my_frozenset)


## Boolean Type:

# Boolean
is_true = True
print(is_true)


is_false = False
print(is_false)

